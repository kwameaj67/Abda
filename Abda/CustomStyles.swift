import Foundation
import UIKit

func roundButtonCorners(button: UIButton){
    button.layer.cornerRadius = button.layer.frame.height / 2
}

func roundViewCorners(view: UIView){
    view.layer.cornerRadius = view.layer.frame.height / 2
}

